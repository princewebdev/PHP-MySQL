<?php

// addition
$x = 1 + 2;
echo $x , "\n";

// subtraction
$x = 2 - 1;
echo $x , "\n";

// multiplication
$x = 2 * 3;
echo $x , "\n";

// division
$x = 2 / 3;
echo $x , "\n";

/** ***************
 * PEMDAS Stand For -
 *  1. Parentheses
 *  2. Exponents
 *  3. Multiplication / Division
 *  4. Addition / Subtraction
 */

// important of parentheses
$x = 3 + 3 * 10 - 3;
echo $x , "\n";

$x = ( 3 + 3 ) * 10 - 3;
echo $x , "\n";

// exponentiation operator 
$x = 12 ** 2;
echo $x , "\n";
